-- Run once in Supabase SQL Editor if messages do not arrive instantly.
-- The frontend also has a polling fallback, so chat still updates without refresh.
do $$
begin
  if not exists (
    select 1
    from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public'
      and tablename = 'messages'
  ) then
    alter publication supabase_realtime add table public.messages;
  end if;
end $$;
